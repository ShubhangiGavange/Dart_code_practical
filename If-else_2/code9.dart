void main(){
   double marks=50;
   if(marks>=0 && marks<25){
    print("Grade D");
   }else if(marks>=25 && marks<50){
    print("Grade c");
   }else if(marks>=50 && marks<75){
    print("Grade b");
   }else if(marks>=75 && marks<100){
    print("Grade a");
   }else{
    print("invalid marks");
   }
}